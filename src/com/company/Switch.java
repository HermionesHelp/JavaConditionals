package com.company;

public class Switch {

    public static void main(String[] args) {
        // switch on an int
        int someNumber = 1;
        switch(someNumber) {
            case 1:
                System.out.println("The number was 1");
                break;
            case 2:
                System.out.println("The number was 2");
                break;
        }
    }
}